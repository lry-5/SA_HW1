package com.yunpan.entity;
/**
 * 文件夹信息实体类
 * @author pamgo
 * @version v1.0
 */
public class FoldlerInfo {
	public FoldlerInfo() {
	}
	private int id;
	
	private String foldername;
	
	private String path; //文件夹路径
	
	private String parent_path; //上层目录
	
	private Long created_time; //创建时间
	
	private int create_userid; //创建人

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFoldername() {
		return foldername;
	}

	public void setFoldername(String foldername) {
		this.foldername = foldername;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getParent_path() {
		return parent_path;
	}

	public void setParent_path(String parent_path) {
		this.parent_path = parent_path;
	}

	public Long getCreated_time() {
		return created_time;
	}

	public void setCreated_time(Long created_time) {
		this.created_time = created_time;
	}

	public int getCreate_userid() {
		return create_userid;
	}

	public void setCreate_userid(int create_userid) {
		this.create_userid = create_userid;
	}
	
	
}
